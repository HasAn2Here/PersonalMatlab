function Z = computeZ(P, T, Sg)
  
    A1 = -0.001946; A2 = -0.027635; A3 = -0.136315;
    A4 = -0.23849; A5 = 0.105168; A6 = 3.44e4;

   
    F1 = P .* (0.251 * Sg - 0.15) - 0.202 * Sg + 1.106;
    F2 = 1.4 .* exp(-0.0054 * (T - 460));
    F3 = A1 .* P.^5 + A2 .* P.^4 + A3 .* P.^3 + A4 .* P.^2 + A5 .* P;
    F4 = (0.154 - 0.152 * Sg) .* ((P.^(1.85) - 1) .* exp(-0.5 * P) - 0.02);
    F5 = 0.35 .* (0.6 - Sg) .* exp(-2.039 .* (P - 1).^2);


    Z = F1 ./ (1 + ((A6 .* P .* 10.^(2.785 .* Sg) ./ T.^3.825) + F3 + F4 + F5));
end